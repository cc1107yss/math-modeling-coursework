% 元胞自动机 
% 森林火灾模型规则:
% (1) 正在燃烧的树变成空格位;
% (2) 如果绿树格位的最近邻居中有一个树在燃烧, 则它依概率 q 变成正在燃烧的树;
% (3) 在空格位, 树以概率 p 生长;
% (4) 在最近的邻居中没有正在燃烧的树的情况下树在每一时步以概率 f (闪电) 变为正在燃烧的树.
% S == 2 燃烧
% S == 1 绿树
% S == 0 空地
clear, clc, close all;%清空工作区变量 清空命令窗口 关闭所有图形窗口
figure;%打开一个新的图像窗口
p = 0.00001;%长树
q = 0.7;%邻居点燃
f = 0.005;%闪电自燃
M = 100;	% 森林边长
rng('default');%把随机数生成器恢复到默认状态。如果只写这一句，那么每次运行结果都一样。
rng('shuffle');%用当前时间重新设置随机种子。如果写这一句，那么每次运行结果都不同。
S = randi([0, 1], M);	% 随机初始化森林状态 生成一个 100×100 的随机整数矩阵，每个元素只能是0or1
Sk = zeros(M + 2);%建立了一个102*102的全零矩阵
Sk(2 : M + 1, 2 : M + 1) = S;	% 加边开始的森林初值 这是为了使边界上的元胞使用一样的判定规则
%把原来的 100×100 森林 S 放到中间
% 红色表示正在燃烧(S中等于2的位置) 
% 绿色表示绿树(S中等于1的位置) 
% 黑色表示空格位(S中等于0的位置)
C = zeros(M + 2, M + 2, 3);%创建一个 102 × 102 × 3 的三维数组 三层图像
R = zeros(M);%把绿树位置在绿色通道里设为 1
G = zeros(M);%先创建两个 100 × 100 的矩阵 红色绿色通道
R(S == 2) = 1;%把 S 中燃烧状态的位置，在红色通道里设为 1
G(S == 1) = 1;
C(2 : M + 1, 2 : M + 1, 1) = R;   %红色图层 把红色通道 R 填进图像 C 的第 1 层（红色层）中间区域
C(2 : M + 1, 2 : M + 1, 2) = G;   %绿色图层 把绿色通道 G 填进图像 C 的第 2 层（绿色层）中间区域
Ci = imshow(C, 'InitialMagnification', 'fit');%把图像 C 显示出来
%'InitialMagnification', 'fit'表示自动缩放，让整个图像正好适应窗口
tp = title('T = 0');	% 时间记录 给图像加标题，初始时间是 T = 0
% 在循环前添加
videoObj = VideoWriter('forest_fire.avi'); %创建一个视频对象，准备输出成 forest_fire.avi
videoObj.FrameRate = 10; % 设置帧率 每秒 10 帧
open(videoObj);%正式打开这个视频文件，后面才能往里面写帧
for ti = 1 : 100
	%% (1)
	St = Sk;	% St表示t时刻的森林情况 先把当前旧状态 Sk 复制给下一时刻新状态 St
    %因为更新时不能边扫边改旧状态，不然前面改掉的会影响后面判断
	St(Sk == 2) = 0;%把所有旧状态里"燃烧的树"变成空地

	%% (2)被燃烧
	Su = zeros(M + 2);%新建一个 102 × 102 矩阵 Su，后面用来记录"每个格子周围有几个燃烧邻居"
	Sf = Sk;	%复制旧状态 Sf表示模拟着火的过程
	Sf(Sf < 1.5) = 0;	% 只留下着火点
	Sf = Sf / 2;	% 着火点变为1, 此处Sf只有着火和空格两种 让 Sf 变成一个"燃烧点指示矩阵"
	% 平移后八个方向叠加, 记录下Su周围八个点, 有多少个在燃烧
	Su(2 : M + 1, 2 : M + 1) = ( ...
		  Sf(1 : M, 1 : M)		   + Sf(1 : M, 2 : M + 1) ...
		+ Sf(1 : M, 3 : M + 2)	   + Sf(2 : M + 1, 1 : M) ...
		+ Sf(2 : M + 1, 3 : M + 2) + Sf(3 : M + 2, 1 : M) ...
		+ Sf(3 : M + 2, 2 : M + 1) + Sf(3 : M + 2, 3 : M + 2) ...
	) .* Sk(2 : M + 1, 2 : M + 1);%把燃烧矩阵往不同方向错开，再叠加
    %求和得到：每个内部格子周围有多少个邻居在燃烧 逐元素乘当前状态 找"有燃烧邻居且本身不是空地"的位置。
	d = find(Su > 0.5);%"周围有燃烧邻居、并且当前位置可能是树"的格子
	d_len = length(d);%统计这些位置有多少个
	r = rand(d_len, 1);%给这些位置各生成一个随机数，用来决定是否被点燃
	Su(d) = round(2 * (r <= q) + (r > q));%这些候选格子被赋成：2点燃成功or1还是树
%所以 Su 其实被构造成一个"更新掩码"：•	若某位置应该点燃 → Su=2 •	若某位置不变 → Su=1
	St = St .* Su;	% Sf -> St着火, 空白, 树 把"有邻居燃烧导致的点火"一次性批量完成

	%% (3)长树
    % 将森林的空白处变为1, 其他地方为0
	Se = Sk(2 : M + 1, 2 : M + 1);%取旧状态
	Se(Se < 0.5) = 4;	% 空白地方赋值为4
	Se(Se < 3) = 0;	% 有树和着火赋值为0
	Se(Se > 3) = 1;	% 空白地方赋值为1
	% 长树, 更新t时刻的森林St
	St(2 : M + 1, 2 : M + 1) = St(2 : M + 1, 2 : M + 1) + Se .* (rand(M) < p);
    %只有原来是空地的位置，才有机会乘出 1 加到st上长树

	%% (4)周围没有燃烧的树，自然着火的树
	Ss = zeros(M + 2);
    Ss(sk==2)=1;
    %把旧状态中所有燃烧点标成 1，其余为 0。这样 Ss 就是"燃烧点分布图"
	% 平移后八个方向叠加, 记录下Ss周围八个点, 有多少个树在燃烧
	Ss(2 : M + 1, 2 : M + 1) = ...
		  Ss(1 : M, 1 : M)		   + Ss(1 : M, 2 : M + 1) ...
		+ Ss(1 : M, 3 : M + 2)	   + Ss(2 : M + 1, 1 : M) ...
		+ Ss(2 : M + 1, 3 : M + 2) + Ss(3 : M + 2, 1 : M) ...
		+ Ss(3 : M + 2, 2 : M + 1) + Ss(3 : M + 2, 3 : M + 2);
    %为了判断"周围有没有燃烧邻居"
	Ss(Ss > 0.5) = 1;
	Ss(Ss < 0.5) = 0;%只要周围有至少一个燃烧邻居，就设成 1
	d = find(Ss == 0 & St == 1); %周围没有燃烧的树（Ss == 0)，目前还绿着（St == 1)
	d_len = length(d);%统计数量
	r = rand(d_len, 1);%为这些树生成随机数，决定是否闪电自燃
	St(d) = round(2 * (r <= f) + (r > f)); %以一定概率自燃

	%% 更新t时刻的森林St
	Sk = St;
	R = zeros(M + 2);
	G = zeros(M + 2);%新建与 Sk 同大小的红色、绿色通道
	R(Sk == 2) = 1;
	G(Sk == 1) = 1;
	C(:, :, 1) = R;
	C(:, :, 2) = G;%把红色、绿色通道写进图像 C
	set(Ci, 'CData', C);%更新已有图像对象 Ci 的内容为当前图像 C
	set(tp, 'string', ['T = ', num2str(ti)]);%把标题更新成当前时间步
	pause(0.01);
    % 在循环内添加（放在set(tp...)之后）
    frame = getframe(gcf);
    writeVideo(videoObj, frame);%抓取当前帧并写入视频
end
% % 在循环结束后添加
close(videoObj);%正式结束写入，把视频文件封口保存
